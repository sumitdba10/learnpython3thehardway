# Python Escape Sequence

# \a is ASCII BEL (BELL) escape sequence
print('Sumit\a hello')

# \b is backspace escape sequence, it backspace cursor 1 position in string
print('Sumitt\b hello')

#\t is tab escape sequence, it add a tab space in front of string
print('\t hello')

# \f is ASCII formfeed
# Form feed is also a special character that, when encountered in code, causes printers to automatically advance one full page or the start of the next page.
#This form feed special character has a decimal value of 12 in the ASCII character set.
print('Sumit\f hello')

#\
print('Sumit\v hello')
